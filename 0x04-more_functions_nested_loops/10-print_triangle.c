#include "main.h"
#include <stdio.h>

/**
 * print_diagonal - prints numbers from 0 to 9
 * @size: number of triangles
 * Return: returns 0 or 1
 */
void print_triangle(int size)
{
	if (size <= 0)
	{
		_putchar('\n');
	} else
	{
		int i, j;

		for (i = 1; i <= size; i++)
		{
			for (j = i; j < size; j++)
			{
				_putchar(' ');
			}
			for (j = 1; j <= i; j++)
			{
				_putchar('#');
			}
			_putchar('\n');
		}
	}
}
